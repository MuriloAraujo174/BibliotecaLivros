    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome, cpf, email, senha;
        nome = txtNome.getText();
        cpf = txtCpf.getText();
        email = txtEmail.getText();
        senha = txtSenha.getText();
        
        boolean validNome = false, 
                validCpf = false, 
                validEmail = false, 
                validSenha = false;
        
        try {
            
            if(!nome.isEmpty() && nome != null) {
                validNome = true;
            }
            
            if(cpf != null && !cpf.isEmpty()) {
                validCpf = true;
            } 
            
            if(!email.isEmpty() && email != null) {
                validEmail = true;
            }
            
            if(!senha.isEmpty() && senha != null) {
                validSenha = true;
            }
            
            CadastroFuncionariosDao cadastro = new CadastroFuncionariosDao();
        
            if(validNome == true && validCpf == true && validEmail == true && validSenha == true) {
                
                cadFuncionario.getPerson().setNome(nome);
                cadFuncionario.getCpf().setCpf(cpf);
                cadFuncionario.getLogin().setEmail(email);
                cadFuncionario.getLogin().setSenha(senha);
                
                if(gerente.isSelected()) {
                    cadFuncionario.getTypeUser().setTyperUser("gerente");
                } else {
                    cadFuncionario.getTypeUser().setTyperUser("Funcionario");
                }

                cadastro.register(cadFuncionario);
            
                JOptionPane.showMessageDialog(null, "Usu√°rio cadastrado com sucesso!");
                
                new Login().setVisible(true);
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no cadastro! " + e);
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

